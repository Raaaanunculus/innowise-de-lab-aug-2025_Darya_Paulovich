# запрос ввода целого числа пользователем
# функция input() возвращает строку, int() преобразует её в целое число
number = int(input("введите целое число: "))

# проверка условия: если число делится на 2 без остатка
# оператор % возвращает остаток от деления
# если остаток равен 0, значит число четное
if number%2 == 0:
    # вывод сообщения, что число четное
    print("четное")
    
# все остальные случаи (когда остаток от деления не равен 0)
else:
    # вывод сообщения, что число нечетное
     print("нечетное")
     
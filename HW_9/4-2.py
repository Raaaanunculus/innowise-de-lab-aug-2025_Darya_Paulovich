def add_vat(price, vat_rate=0.20):
    # вычисляем итоговую цену: цена + (цена * ндс)
    # и возвращаем результат с помощью return
    return price + (price * vat_rate)

# создаем список исходных цен без ндс
prices = [1000, 3499, 250]
# задаем ставку ндс 
nds = 0.20

# выводим заголовок для результатов
print("итоговые цены с НДС:")

# проходим по каждой цене в списке 
for price in prices:
    # для каждой цены вызываем функцию add_vat, передавая цену и ставку ндс
    # результат работы функции сохраняем в переменную final_price
    final_price = add_vat(price, nds)
    # выводим итоговую цену с ндс
    print(final_price)
    
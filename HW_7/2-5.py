# запрашиваем у пользователя высоту прямоугольника и преобразуем в число
height = int(input("Введите высоту прямоугольника: "))

# запрашиваем у пользователя ширину прямоугольника и преобразуем в число
width = int(input("Введите ширину прямоугольника: "))

# внешний цикл для количества строк (по высоте)
for i in range(height):
    # внутренний цикл для количества звездочек в строке (по ширине)
    for j in range(width):
        # выводим звездочку без перехода на новую строку
        print("*", end="")
    # после завершения внутреннего цикла переходим на новую строку
    print()
    
# создаем список продуктов для покупки
shopping_list = ["milk", "bread", "eggs", "butter", "apples"]

# выводим заголовок "список покупок"
print("Список покупок:")

# создаем счетчик и начинаем с 1
i = 1

# начинаем цикл для каждого продукта в списке покупок
for product in shopping_list:
    # выводим номер и название продукта
    print(f"{i}. {product}")
    # увеличиваем счетчик на 1 для следующего продукта
    i = i + 1
    
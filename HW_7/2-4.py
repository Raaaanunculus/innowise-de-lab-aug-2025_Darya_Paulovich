scores = [75, 88, -10, 95, 100, -25, 89]

# создаем переменную для подсчета общей суммы баллов
total_score = 0

# начинаем цикл перебора всех баллов в списке
for score in scores:
    # проверяем, является ли балл отрицательным
    if score < 0:
        # пропускаем отрицательный балл и переходим к следующему
        continue
    # проверяем, равен ли балл нулю
    elif score == 0:
        # прерываем цикл при встрече нуля
        break
    # если балл положительный и не ноль
    else:
        # выводим сообщение о добавлении балла
        print(f"Добавлен балл: {score}")
        # добавляем текущий балл к общей сумме
        total_score = score + total_score

# выводим сообщение об успешной обработке
print(f"Все данные успешно обработаны")

# выводим итоговую сумму баллов
print(f"\nИтоговая сумма баллов: {total_score}")

secret_number = 7

# создаем пустую переменную для хранения ответа пользователя
user_input = ""

# запускаем цикл, который будет работать пока ответ не совпадет с секретным числом
while user_input != secret_number:
    # просим пользователя угадать число
    print("Угадайте число от 1 до 10:")
    # получаем ввод пользователя и преобразуем текст в число
    user_input = int(input())
    
    # проверяем, не угадал ли пользователь
    if user_input != secret_number:
        # если не угадал, выводим сообщение об ошибке
        print("Неверно, попробуйте еще раз.")
    else:
        # если угадал, выводим поздравление
        print("Поздравляю! Вы угадали число!")
        
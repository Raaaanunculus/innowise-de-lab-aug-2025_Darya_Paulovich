# создаем переменную и сохраняем в ней строку для работы
given_string = "Python Programming"

# выводим длину строки с помощью функции len()
print(f"1. длина строки: {len(given_string)}")

# получаем символ, который находится на позиции с индексом 7 
# и выводим его в кавычках, чтобы было лучше видно
print(f"2. символ по индексу 7: '{given_string[7]}'") 

# берем три последних символа строки
print(f"3. последние 3 символа: '{given_string[-3:]}'") 

# проверяем, есть ли внутри большой строки маленькая строка "gram"
substring = "gram" # сначала сохраняем искомую подстроку в переменную
# оператор in проверяет вхождение и возвращает true или false 
is_present = substring in given_string # используем оператор 'in'
# выводим результат проверки
print(f"4. подстрока '{substring}' присутствует? {is_present}") 
